export const getThaiHolidaysForYear = (year: number): Record<string, string> => {
  const result: Record<string, string> = {};
  Object.entries(THAI_HOLIDAYS_2026_2027).forEach(([date, name]) => {
    if (date.startsWith(year.toString())) {
      result[date] = name;
    }
  });
  return result;
};

